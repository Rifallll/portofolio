-- Buat storage bucket untuk nyimpen file
INSERT INTO storage.buckets (id, name, public) VALUES ('cv-files', 'cv-files', true) ON CONFLICT (id) DO NOTHING;
INSERT INTO storage.buckets (id, name, public) VALUES ('project-images', 'project-images', true) ON CONFLICT (id) DO NOTHING;

-- Buka akses public untuk storage dan tabel
ALTER TABLE cv_uploads ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow public read cv" ON cv_uploads FOR SELECT USING (is_active = true);
CREATE POLICY "Allow auth all cv" ON cv_uploads FOR ALL USING (auth.role() = 'authenticated');

CREATE POLICY "Allow public read cv files" ON storage.objects FOR SELECT USING (bucket_id = 'cv-files' OR bucket_id = 'project-images');
CREATE POLICY "Allow auth all files" ON storage.objects FOR ALL USING (auth.role() = 'authenticated');

