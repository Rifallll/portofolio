-- 1️⃣ HAPUS TABEL LAMA (JIKA ADA) BIAR BERSIH
DROP TABLE IF EXISTS projects;
DROP TABLE IF EXISTS experience;
DROP TABLE IF EXISTS skills;
DROP TABLE IF EXISTS certificates;
DROP TABLE IF EXISTS cv_uploads;

-- ==============================================================================
-- 2️⃣ BUAT SEMUA TABEL
-- ==============================================================================

-- A. TABEL PROJECTS
CREATE TABLE projects (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  category TEXT DEFAULT 'Web Dev',
  tech JSONB DEFAULT '[]'::jsonb,
  image TEXT,
  year TEXT,
  featured BOOLEAN DEFAULT false,
  client TEXT,
  "desc" TEXT,
  demo_url TEXT,
  repo_url TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- B. TABEL EXPERIENCE
CREATE TABLE experience (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  role TEXT NOT NULL,
  company TEXT NOT NULL,
  period TEXT,
  year TEXT,
  "desc" TEXT,
  tech JSONB DEFAULT '[]'::jsonb,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- C. TABEL SKILLS
CREATE TABLE skills (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category TEXT NOT NULL,
  name TEXT NOT NULL,
  proficiency INTEGER DEFAULT 80,
  icon_name TEXT DEFAULT 'Code',
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- D. TABEL CERTIFICATES
CREATE TABLE certificates (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  org TEXT,
  year TEXT,
  category TEXT DEFAULT 'Engineering',
  image_url TEXT,
  credential_url TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- E. TABEL CV UPLOADS (UNTUK FILE PDF)
CREATE TABLE cv_uploads (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    file_url TEXT NOT NULL,
    file_name TEXT NOT NULL,
    uploaded_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Hanya boleh ada satu CV aktif
CREATE UNIQUE INDEX idx_active_cv ON cv_uploads (is_active) WHERE is_active = true;

